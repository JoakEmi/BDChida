    private void BtnSiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSiActionPerformed
        // TODO add your handling code here:
        int id;
        if(!txtProducto.getText().equals("") && !txtCantidad.getText().equals("")){
            try{

                Connection con1;
                Conexion conect1 = new Conexion();
                con1 = conect1.getConnection();
                
                //String sql2 = "SELECT MAX(NumeroCompra) AS LargestID FROM Compras";
                String sql2 = "SELECT NumeroCompra FROM Compras";
                try(Statement st = con1.createStatement(); ResultSet rs = st.executeQuery(sql2)){
                   id = rs.getInt("NumeroCompra");
                }
                
                String sql = "INSERT INTO DetalleCompra (NumeroCompra, CodigoProducto, Cantidad)"
                            + "VALUES('" + String.valueOf(id) + "','" + txtProducto.getText() + "','" + txtCantidad.getText() +"')";
                try (Statement st = con1.createStatement()) {
                    st.executeUpdate(sql);
                }
             
                conect1.Desconexion();
                 
             }catch(SQLException e){
                 JOptionPane.showMessageDialog(null, e,
                        "Error", JOptionPane.OK_OPTION);
             
             }
        }else
            JOptionPane.showMessageDialog(null, "Inserta todos los datos",
                        "Error", JOptionPane.OK_OPTION);
            
       txtProducto.setText("");
       txtCantidad.setText("");
        //this.dispose();
    }//GEN-LAST:event_BtnSiActionPerformed

